                <h2>Powder skis</h2>

                <p>Powder skis differ from all mountain skis in shape and size, but not in overall construction. They are usually wider and have more tip rocker (rocker is when the ski is shaped somewhat like a banana and the tips rise gradually off the ground) to make the ski float more in deep powder. You may also find that most powder skis are more asymmetrical than all mountain skis, often with the front being wider than the back, and this is also to achieve better floatation. In the selection of powder skis we have to offer, you will find skis that glide effortlessly through powder while still being light and agile.</p>
            </section>

            <!--Park skis-->
            <section>
                <h2>Park skis</h2>

                <p>Park skis have some of the most variation of any type of ski. One thing most park skis have in common is they are more symmetric than all-mountain skis, so that the rider can ski as confidently backwards as they can forwards. Most park skis that are geared towards large jumps and high speeds are similar to all-mountain skis apart from their shape. The biggest difference is in skis that are geared towards rails and playful buttering (buttering is when the skier presses into the noses or tails in order to lift the rest of the ski off the ground and pop into the air). These skis are much more flexible and have more rocker than other skis, which makes them extremely fun in the park but usually takes away from their all-mountain performance. This makes park skis a bit harder to select, but that’s why I’ve designed the quiz on the next page.</p>
